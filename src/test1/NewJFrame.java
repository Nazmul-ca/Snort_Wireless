    private ArrayList mycomboDisplay(){
        ArrayList<String> lastToken = new ArrayList<>(); 
        try{
            Process proc = runtime.exec("dumpcap -D");
            BufferedReader stdInput = new BufferedReader(new 
                 InputStreamReader(proc.getInputStream()));
            //BufferedReader stdError = new BufferedReader(new 
            //     InputStreamReader(proc.getErrorStream()));
            String s = null;
            while ((s = stdInput.readLine()) != null) {
                lastToken.add(s.substring(s.lastIndexOf(". ") + 1));
            }
        }catch(Exception e){}
        return lastToken;
    }

